package lost;

public class Lost {
	private int boardID;
	private int lostID;
	private String lostTitle;
	private String userID;
	private String lostDate;
	private String lostContent;
	private int lostAvailable;
	public int getBoardID() {
		return boardID;
	}
	public void setBoardID(int boardID) {
		this.boardID = boardID;
	}
	public int getLostID() {
		return lostID;
	}
	public void setLostID(int lostID) {
		this.lostID = lostID;
	}
	public String getLostTitle() {
		return lostTitle;
	}
	public void setLostTitle(String lostTitle) {
		this.lostTitle = lostTitle;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getLostDate() {
		return lostDate;
	}
	public void setLostDate(String lostDate) {
		this.lostDate = lostDate;
	}
	public String getLostContent() {
		return lostContent;
	}
	public void setLostContent(String lostContent) {
		this.lostContent = lostContent;
	}
	public int getLostAvailable() {
		return lostAvailable;
	}
	public void setLostAvailable(int lostAvailable) {
		this.lostAvailable = lostAvailable;
	}

	

}
